num_1 = float(input('enter number 1= '))
num_2 = float(input('enter number 2= '))

choice = input('Enter your Choice + - * = ')
if choice == '+':
    print(f'addition:{num_1 + num_2}')
elif choice == '-':
    print(f'Substraction : {num_1 - num_2}')
elif choice == '*':
    print(f'Substraction : {num_1 * num_2}')
else:
    print('Invalid Choice')
